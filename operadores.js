// Operadores Matemáticos 
// + - * / % **
// += -= *= /= %=

console.log(10 + 10)
console.log(10 - 10)
console.log(10 / 10)
console.log(10 * 10)
console.log(10 % 10)
console.log(10 ** 10) //Potencia


let num = 15;
console.log(num += 5); // num = num + 5
console.log(num -= 5); 
console.log(num *= 5);
console.log(num /= 5);
console.log(num %= 5);


//Operadore booleanos
// >< >= <= != ===

console.log(1 > 2);
console.log(1 < 2);
console.log(1 >= 2);
console.log(1 <= 2);
console.log(1 !== 2);
console.log(1 === 2);

//Operadores lógicos
// and && 
console.log(true && true);
// or ||
console.log(true || false);
// not !
console.log(!true);
